
public class Shortcuts {

	public static void keyBoardShortCuts() {
		System.out.println("******* Shortcuts *********");
		System.out.println("CTRL + SHIFT + /: comment block of code - ");
		System.out.println("CTRL + \\ : UNcomment block of code - ");
		System.out.println("CTRL + D: delete current line selection ");
		System.out.println("ALT + up/down arrow: moves row/entire selection to up or down");
		System.out.println("CTRL + E: list of all open editors");
		System.out.println("*** CTRL + 1: most useful, it will show quick fix options, place cursor on error line and press CTRL + 1");
		System.out.println("*** CTRL + SHIFT + T: press and search a classname");
		System.out.println("CTRL + ALT + Down Arrow: Copy Lines");
		System.out.println("CTRL + M: Maximize or umaximize your current tab");
		
		System.out.println("F3: place cursor on method and press F3 it will take to respective method declaration");
		System.out.println("sysout(type) + CTRL + space: shortcut for system.out.println");

		System.out.print("\n");
	}

}
